#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	*h;
	int		i;

	h = (char*)s;
	i = 0;
	while (n--)
	{
		if (h[i] == c)
			return (&h[i]);
		i++;
	}
	return (NULL);
}
