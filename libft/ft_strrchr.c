#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *g;
	char *k;

	g = NULL;
	k = (char*)s;
	while (*k)
	{
		if (*k == c)
		{
			g = (char*)k;
		}
		k++;
	}
	if (c == '\0')
	{
		return (k);
	}
	return (g);
}
